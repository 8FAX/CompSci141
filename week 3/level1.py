def bow(h):
    t.pendown()
    t.left(30)
    t.forward(h)
    t.right(120)
    t.forward(h)
    t.right(120)
    t.forward(2*h)
    t.left(120)
    t.forward(h)
    t.left(120)
    t.forward(h)
    t.right(30)
    t.penup()

bow3(100)
t.done()